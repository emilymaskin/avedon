Michael Avedon, a graduate of the International Center of Photography, has begun to develop his 
photographic perspective over the past four years in New York City.
Working primarily with black &amp; white 35mm and medium format film, his portfolio thus far
depicts portraits of artists, creators, politicians, and relatives.
A determination to expound his visual understanding of everything from performance 
to the natural world has allowed him to move forward with an exploratory approach,<br /> 
informed by the photographic legacy of his grandfather Richard Avedon.
</p>

